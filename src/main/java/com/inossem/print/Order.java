package com.inossem.print;

public class Order {
	
	private String batchCode;
	private String purNo;
	private String supplierDesc;
	private String contractNo;
	private String reqDept;
	private String materielCode;
	private String materielDesc;
	private String inStorageDate;
	public String getBatchCode() {
		return batchCode;
	}
	public void setBatchCode(String batchCode) {
		this.batchCode = batchCode;
	}
	public String getPurNo() {
		return purNo;
	}
	public void setPurNo(String purNo) {
		this.purNo = purNo;
	}
	public String getSupplierDesc() {
		return supplierDesc;
	}
	public void setSupplierDesc(String supplierDesc) {
		this.supplierDesc = supplierDesc;
	}
	public String getContractNo() {
		return contractNo;
	}
	public void setContractNo(String contractNo) {
		this.contractNo = contractNo;
	}
	public String getReqDept() {
		return reqDept;
	}
	public void setReqDept(String reqDept) {
		this.reqDept = reqDept;
	}
	public String getMaterielCode() {
		return materielCode;
	}
	public void setMaterielCode(String materielCode) {
		this.materielCode = materielCode;
	}
	public String getMaterielDesc() {
		return materielDesc;
	}
	public void setMaterielDesc(String materielDesc) {
		this.materielDesc = materielDesc;
	}
	public String getInStorageDate() {
		return inStorageDate;
	}
	public void setInStorageDate(String inStorageDate) {
		this.inStorageDate = inStorageDate;
	}
	
	

}
